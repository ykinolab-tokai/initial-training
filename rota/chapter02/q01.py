# 以下の FUNC という部分を組み込み関数に置き換えてください

# リスト a の長さを求める。
a = [4, 8, 3, 4, 1]
res = len(a)
print(res)

# リスト a に含まれる値の最大値を求める。
a = [4, 8, 3, 4, 1]
res = max(a)
print(res)

# リスト a に含まれる値の最小値を求める。
a = [4, 8, 3, 4, 1]
res = min(a)
print(res)

# リスト a に含まれる値の合計値を求める。
a = [4, 8, 3, 4, 1]
res = sum(a)
print(res)

# リスト a をソートして、[1, 3, 4, 4, 8] というリストを返す。
a = [4, 8, 3, 4, 1]
res = sorted(a)
print(res)